# Desenvolva um programa que leia duas notas de um aluno, calcule e mostre a sua média
# aritmética

nota1 = int(input('Digite sua primeira nota: ')) # valor inserido pelo usuário
nota2 = int(input('Digite sua outra nota: ')) # valor inserido pelo usuário
media = (nota1+nota2)/2
print(f'a media do aluno foi de: {media}')
